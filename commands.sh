#!/bin/bash

# Print current working directory
pwd

# Change directory to home directory
cd

# List files and directories in current directory
ls

# Print environment variables
env

# Exit the shell
exit

# Change directory to a specific path
cd /path/to/directory

# List files and directories using absolute path
/bin/ls

# List files and directories with long format using absolute path
/bin/ls -l

# List files and directories with long format in /tmp directory
ls -l /tmp
